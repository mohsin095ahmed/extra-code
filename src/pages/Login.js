const defaultTheme = createTheme();

export default function SignIn() {

  const [email, setemail] = React.useState("");
  const [password, setpassword] = React.useState("");
  const [bool, setbool] = React.useState(false)
  const router = useRouter();
React.useEffect(()=>{
  
  const item = localStorage.getItem("user-Token");
  
  item && router.push('/')
},[bool])




  const handleSubmit = async(event) => {
    event.preventDefault();
    setemail("");
    setpassword("")
    
    await axios.post("https://cute-ruby-penguin-hat.cyclic.app/api/user/login",{
    
      email,
      password

     },{
      headers: {
        'Content-Type': 'application/json'

      }
     }).then( res =>{
      console.log(res.data.user)
       alert("sign in sucsseful");
        localStorage.setItem("user-Token", res.data.token);
        localStorage.setItem("user", JSON.stringify(res.data.user));
        localStorage.setItem("user-roll", JSON.stringify(res.data.user.roll))
        setbool(true)
     }).catch(err =>{
      
      console.log("errr----<", err.response.data)
      alert(err.response.data.user) } )

  };

  return (
    <ThemeProvider theme={defaultTheme}>
      <Container component="main" maxWidth="xs">
        <CssBaseline />
        <Box
          sx={{
            marginTop: 8,
            display: 'flex',
            flexDirection: 'column',
            alignItems: 'center',
          }}
        >
          <Avatar sx={{ m: 1, bgcolor: 'secondary.main' }}>
            <LockOutlinedIcon />
          </Avatar>
          <Typography component="h1" variant="h5">
            Sign in
          </Typography>
          <Box component="form" onSubmit={handleSubmit} noValidate sx={{ mt: 1 }}>
            <TextField
              margin="normal"
              required
              fullWidth
              id="email"
              label="Email Address"
              name="email"
              autoComplete="email"
              autoFocus
              value={email}
              onChange={(e)=>{ setemail(e.target.value)}}
            />
            <TextField
              margin="normal"
              required
              fullWidth
              name="password"
              label="Password"
              type="password"
              id="password"
              autoComplete="current-password"
              value={password}
              onChange={(e)=>{ setpassword(e.target.value)}}
            />
            {/* <FormControlLabel
              control={<Checkbox value="remember" color="primary" />}
              label="Remember me"
            /> */}
            <Button
              type="submit"
              fullWidth
              variant="contained"
              sx={{ mt: 3, mb: 2 }}
            >
              Sign In
            </Button>
            <Grid container>
              {/* <Grid item xs>
                <Link href="#" variant="body2">
                  Forgot password?
                </Link>
              </Grid> */}
              <Grid item>
                <Link href="#" variant="body2">
                  {"Don't have an account? Sign Up"}
                </Link>
              </Grid>
            </Grid>
          </Box>
        </Box>
        {/* <Copyright sx={{ mt: 8, mb: 4 }} /> */}
      </Container>
    </ThemeProvider>
  );
}